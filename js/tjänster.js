function hoverInfo1() {
  var x = document.getElementById("card-info");
  var y = document.getElementById("card-info-hover");
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
    y.style.display = "none";
  }
}

